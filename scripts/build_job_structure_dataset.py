def main() -> None:
    cities = pd.read_csv(INPUT)
    rows = []
    for _, city in cities.iterrows():
        city_name = city["city_name_zh"]
        base_salary = float(city["avg_salary"])
        profile = PROFILES.get(city_name, REGION_DEFAULTS.get(city["region"], REGION_DEFAULTS["华东"]))
        for industry, position, share, factor, education, experience in profile:
            rows.append(
                {
                    "city_name_zh": city_name,
                    "region": city["region"],
                    "city_tier": city["city_tier"],
                    "industry": industry,
                    "position_type": position,
                    "job_share_percent": share,
                    "position_avg_salary_rmb": round(base_salary * factor, 0),
                    "education_requirement": education,
                    "experience_requirement": experience,
                    "job_structure_source": "课程项目就业结构补充表：依据招聘平台公开报告常见行业类别和城市产业定位整理，用于可视化展示，不代表实时全量岗位。参考来源包括 Job-SDF benchmark 与招聘平台公开薪酬报告。",
                }
            )
    out = pd.DataFrame(rows)
    OUTPUT.parent.mkdir(parents=True, exist_ok=True)
    out.to_csv(OUTPUT, index=False, encoding="utf-8-sig")
    print(OUTPUT)


if __name__ == "__main__":
    main()
